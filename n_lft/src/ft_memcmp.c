#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((__uint8_t*)s1)[i] != ((__uint8_t*)s2)[i])
			return (((__uint8_t*)s1)[i] - ((__uint8_t*)s2)[i]);
		i += 1;
	}
	return (0);
}

#include "vector.h"

t_float4	cross(t_float4 a, t_float4 b)
{
	return ((t_float4){
		a.y * b.z - a.z * b.y,
		a.z * b.x - a.x * b.z,
		a.x * b.y - a.y * b.x,
		0
	});
}
